    @Override
    public void actionPerformed(ActionEvent e) {
        var obj = e.getSource();
        if (obj.equals(btnAddHS)) {
            SuaHoSo();
        } else if (obj.equals(btnCancelHS)) {
            dispose();
        } 
    }
    
